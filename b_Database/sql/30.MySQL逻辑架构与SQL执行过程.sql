--  MySQL逻辑架构与SQL执行过程

	-- 一.逻辑架构
		-- 1.三层结构：
		-- 第一层：连接层、第二层：服务层、第三层：引擎层、外加：存储层
			-- 连接层：提供连接池(TCP连接池限制连接数，采用长链接模式、线程池控制线程)
			-- 服务层：SQL接口、解析器(分析器)、优化器、查询缓存组件
				-- SQL接口：接受SQL命令(增删改查等等)
				-- 解析器：SQL语句正确则生成解析树，错误则报错
				-- 查询优化器：生成一个执行计划，查询策略：选取-投影-连接
				-- 查询缓存组件：MySQL8.0剔除
			-- 引擎层：与底层的文件系统进行交互
				-- 查看存储引擎
				SHOW ENGINES;
			-- 存储层(不在层结构内)：存储文件及日志等
		
		-- 2.客户端与服务器交互：(按一次交互执行顺序排)
		-- 连接层
			-- (1)客户端连接程序Connectors：JDBC,API,PHP,Python... 
			-- (2)连接池Connection Pool：提供多个用于用户与服务器端交互的线程
		-- 服务层
			-- (3)SQL接口SQL Interface：接收SQL指令，返回查询结果
			-- (4)查询缓存Caches & Buffers：以key-value的方式缓存查询结果
			-- (5)解析器(分析器)Parser：语法解析，语义解析，生成语法树
			-- (6)优化器Optimlzer：核心组件，对SQL进行优化，之后交付执行器
		-- 引擎层
			-- (7)插件式的存储引擎Pluggable Storage Engines：与底层的文件系统进行交互
		-- 存储层
			-- (8)文件系统、日志文件File system、Files & Logs
			-- 顺序：客户端发送指令->服务器：(1)至(8) 
			--	 服务器返回信息->客户端： (8)->(4)->(3)->(1)
			
			
			
	-- 二.SQL执行流程
		-- 1.SQL语句 -> 分析器(语法分析，语义检查)，生成语法树 -> 优化器(逻辑、物理优化)，生成查询计划 -> 执行器 -> 引擎层，底层文件系统查询结果
			-- 语法分析：分析输入的SQL语句是否满足语法规则
			-- 逻辑优化：等价交换SQL语句，更换SQL语句写法使查询效率更高
			-- 物理优化：通过索引和表连接方式优化
			
		-- 2.执行时间分析
			-- (1)查询底层执行的过程
				SELECT @@profiling; 
				
			-- (2)开启控制
				SET profiling = 1; -- 默认为0关闭	
			
			-- (3)查询执行语句
				-- 先执行查询语句
					USE atguigudb;
					SELECT *
					FROM employees;
					SELECT *
					FROM departments;
				-- 查询执行语句
					SHOW PROFILES;
				-- 查询一条指定执行语句
					SHOW PROFILE FOR QUERY 375; -- 查询375条查询记录
			
			
			
	-- 三.数据库缓存池
		-- 1.作用：
			-- (1)将使用频高的热数据加载到缓存池，减少与磁盘直接进行I/O的时间
			-- (2)预读：将查询数据页前后的数据页也一同存放到缓存池中
		
		-- 2.缓存池与查询缓存
			-- (1)缓存池
				-- 数据页，插入缓存，自适应索引哈希，索引页，锁信息，数据字典信息
			-- (2)查询缓存
				-- 将查询结果缓存起来，下次执行完全相同的查询语句时就可以直接得到结果，节省时间。
				-- 由于命中条件苛刻(查询语句必须完全相同(因为查询语句是以字符串方式缓存的)，连空格都必须一致)，在MySQL8.0中已剔除
		
		-- 3.设置、查看缓存池大小
			-- (1)查询大小
				SHOW VARIABLES LIKE 'innodb_buffer_pool_size'; -- 134217728(124MB)
			-- (2)设置大小
				SET GLOBAL innodb_buffer_pool_size = 268435456; -- 256MB
		
		-- 4.多个Buffer Pool实例
			-- 实际中会有多个用户同时访问缓存池，需要进行加锁
		
			-- (1)查看缓存池实例个数
				SHOW VARIABLES LIKE 'innodb_buffer_pool_instances'; -- 默认1
			-- (2)修改实例个数(修改系统配置文件)
				[SERVER]
				innodb_buffer_pool_instances = 2 -- 创建两个实例
					-- 每个用户缓存池大小=总容量/用户实例数
		

	-- 四.存储引擎
		-- 存储引擎就是指表的类型，功能就是接收上层传来的指令，然后对表中的数据进行提取或写入操作。
		
		-- 1.查询存储引擎
			SHOW ENGINES; -- 默认为InnoDB
			
		-- 2.查询默认存储引擎
			SHOW VARIABLES LIKE '%storage_engine%';
			
		-- 3.修改默认存储引擎
			SET default_storage_engine = INNODB;
			
		-- 4.引擎介绍
			-- *(1)InnoDB引擎  	具备外键支持功能的事务存储引擎		
			-- *(2)MyISAM引擎	主要的非事务处理存储引擎
			-- *(3)ARCHIVE引擎  	用于数据存档
			-- (4)BLACKHOLE引擎  	丢弃写操作，读操作会返回空内容
			-- *(5)CSV引擎		存储数据时，以逗号分隔各项数据项		
			-- *(6)MEMORY引擎 	置于内存的表
			-- (7)FEDERATED引擎     访问远程表
			-- (8)MRG_MYISAM引擎  	管理多个myisam表构成的表集合
			-- (9)NDB引擎 		MySQL集群专用存储引擎
	
			-- (1)InnoDB引擎：具备外键支持功能的事务存储引擎	
				-- MySQL5.5及其后默认采用InnoDB引擎
				-- 默认事务型引擎，用于处理大量短期事务，可以确保事务的完整提交与回滚
				-- 进行更新删除操作优先选择InnoDB引擎
				-- 数据文件结构：
					-- 表名.frm、存储表结构
					-- 表名.ibd存储数据和索引
				-- 为处理巨大数据量的最大性能设计
				-- InnoDB引擎写的处理效率稍差于MYISAM引擎，并且会占用更多的磁盘空间(因为同时存储表和索引)
				-- 关注点：事务(并发写、事务、更大资源)
				-- 阿里巴巴使用定制引擎Xtrabd替代Innodb(大公司按需定制，小公司使用开源Innodb)
			
			-- (2)MyISAM引擎：主要的非事务处理存储引擎
				-- MySQL5.5之前默认引擎
				-- 应用于只读或以读为主的业务
				-- 访问速度快
				-- 针对数据统计有额外的常量存储，故而COUNT(*)效率极高(复杂度O(1))
				-- 数据文件结构：
					-- 表名.from存储表结构
					-- 表名.MYD存储数据(MYData)
					-- 表名.MYI存储索引(MYIndex)	
				-- 提供大量特性，全文索引、压缩、空间函数GIS等
				-- 不支持事务、行级锁(仅支持表级锁)、外键，仅缓存索引不缓存数据，崩溃后无法安全恢复
				-- 关注点：性能(节省资源、消耗少、简单业务)
			
			-- (3)Archive引擎：用于数据存档
				-- MySQL5.5之后支持索引功能
				-- 适合日志和数据采集类应用，适合存储大量的独立的作为历史记录的数据，插入速度高
				-- 拥有很好的压缩机制，使用zlib压缩库，文件以表名命名，表名.ARZ
				-- 采用行级锁，支持auto_increment自增列属性
				-- 仅支持查询和插入功能
	
			-- (4)Blackhole引擎：丢弃写操作，读操作会返回空内容
				-- 丢弃所有插入的数据不做保存，记录blackhole日志，通过日志查询或复制数据到备库，但存在问题不推荐
				
			-- (5)CSV引擎：存储数据时，以逗号分隔各项数据项
				-- 可作为一种数据交换的机制
				-- 存储的数据直接可以在操作系统里，用文本编辑器或Excel读取
				-- 对于文件的快速导入导出有明显优势
				-- 不支持索引
				
			-- (6)Memory引擎：置于内存的表
				-- 表结构存储在磁盘中，表数据存储在内存中
				-- 使用场景
					-- 目标数据比较小，频繁访问
					-- 数据是临时的，必须立即可用
					-- 数据丢失没有太大关系
				-- 同时支持哈希索引(默认，比较相等快)和B+数索引(比较范围快)
				-- 表的大小设定参数受限，太大会造成内存溢出(可手动设置参数max_heap_table_size控制memory表大小，默认为16MB)
				-- 数据易丢失(崩溃时数据丢失)，生命周期短，
				-- memory表比MyISAM表快一个数量级
	
			-- (7)Federated引擎：访问远程表
				-- 访问其他MySQL服务器的一个代理，问题多，因此默认禁用
				
			-- (8)Merge引擎：管理多个MyISAM表构成的集合
				
			-- (9)NDB引擎：MySQL集群专用存储引擎
				-- 主要用于MySQL cluster分布式集群环境
				
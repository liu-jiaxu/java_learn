	-- 五.思考
		-- 1.为了减少IO,索引树会一次性加载吗?
			-- (1)数据库索引是存储在磁盘上的,如果数据量很大,必然导致索引的大小也会很大,超过几个G。
			-- (2)当我们利用索引查询时候,是不可能将全部几个G的索引都加载进内存的,我们能做的只能是：逐一加载每一个磁盘页，
			--    因为磁盘页对应着索引树的节点。
			
		-- 2.B+树的存储能力如何?为何说一般查找行记录,最多只需1-3次磁盘IO?
			-- InnoDB存储引擎中页的大小为16KB,一般表的主键类型为INT(占用4个字节)或BIGINT(占用8个字节)，指针类型也一般为4或8个字节，
			-- 也就是说一个页(B+Tree中的一个节点)中大概存储16KB/(8B+8B)=1K个键值(因为是估值，为方便计算，这里的K取值为10^3。也就是
			-- 说一个深度为3的B+Tree索引可以维护10^3 * 10^3 * 10^3 = 10亿条记录。(这里假定一个数据页也存储10^3条行记录数据了)实际
			-- 情况中每个节点可能不能填充满，因此在数据库中，B+Tree的高度一般都在2~4层。MySQL的InnoDB存储引擎在设计时是将根节点常
			-- 驻内存的，也就是说查找某一键值的行记录时最多只需要1~3次磁盘I/O操作。
			
		-- 3.为什么说B+树比B-树更适合实际应用中操作系统的文件索引和数据库索引?
			-- (1)B+树的磁盘读写代价更低B+树的内部结点并没有指向关键字具体信息的指针。因此其内部结点相对B树更小。如果把所有同一内部
			--    结点的关键字存放在同一盘块中，那么盘块所能容纳的关键字数量也越多。一次性读入内存中的需要查找的关键字也就越多。相对
			--    来说10读写次数也就降低了。
			-- (2)B+树的查询效率更加稳定由于非终结点并不是最终指向文件内容的结点，而只是叶子结点中关键字的索引。所以任何关键字的查找
			--    必须走一条从根结点到叶子结点的路。所有关键字查询的路径长度相同，导致每一个数据的查询效率相当。
